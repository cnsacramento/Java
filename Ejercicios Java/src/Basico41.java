/*
Programa Java que muestre los números del 100 al 1 utilizando la instrucción for.
*/
/**
 * @author Christian Novo Sacramento
 */
public class Basico41 {
    public static void main(String[] args) {
        
        for (int i = 100; i > 0; i--) {
            System.out.println(i);
        }
    }
}
